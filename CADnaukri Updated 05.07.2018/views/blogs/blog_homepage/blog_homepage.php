                  <h2 class="display-6 , blog-post-title">Want an exciting career- then it’s time to become a Design Engineer</h2>
                   <p class="blog-post-meta" >21 November 2017 By <a href="#">Shantiswarup Mahapatra</a></p>
                    
                      <figure><img src="<?php echo base_url();?>application/views/blogs/blog_homepage/blog_main1.png" class="img-fluid" alt="blog.img">
                       <figcaption style=" text-decoration: underline; text-align: left;">a) Want an exciting career- then it’s time to become a Design engineer.</figcaption>
                       <figcaption style=" text-decoration: underline; text-align: left;"> Source : internet</figcaption>
                          
          
                      </figure>
                  
                      <p style="text-align: justify;"> It is always pronounced that “Scientists do big discoveries & Engineers smartly implement them into reality”; design engineering is one of the most innovative & lucrative careers for engineers where they can implement all their theoretical knowledge into reality. Engineering is a branch of study which is strongly based on solving of problems in its various spheres. Engineers take up the task after doing thorough background research before assuming charge for the same. It is in the DNA of every engineer; in-fact they collect all the requirements to solve a particular problem. They do some brainstorming, evaluate and choose a solution after all their requirements are met. </p>  
